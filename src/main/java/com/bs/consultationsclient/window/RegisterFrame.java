    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (validateForm()) {
            RabbitMqMessage rabbitMqMessageOut = new RabbitMqMessage();
            rabbitMqMessageOut.setActionCode("ACTION_CODE_0");
            rabbitMqMessageOut.setFirstName(tfFirstName.getText());
            rabbitMqMessageOut.setLastName(tfLastName.getText());
            rabbitMqMessageOut.setEmail(tfEmail.getText());
            rabbitMqMessageOut.setPassword(passwordField.getPassword());
            rabbitMqMessageOut.setReturnQueueId(RETURN_QUEUE_REGISTER_1);
            senderService.send("server", rabbitMqMessageOut);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

