    private boolean validateForm() {
        if (tfFirstName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please provide a first name.", "Form validation error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (tfLastName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please provide a last name.", "Form validation error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (tfEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please provide an e-mail.", "Form validation error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (passwordField.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Please provide a password.", "Form validation error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (!Arrays.equals(passwordField.getPassword(), passwordConfirmField.getPassword())) {
            JOptionPane.showMessageDialog(null, "Confirm password doesn't match the given password.", "Form validation error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

